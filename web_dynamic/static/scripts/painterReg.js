// Function to populate select input with options
function populateSelect(selectElement, options) {
  selectElement.innerHTML = "";
  options.forEach((option) => {
    const optionElement = document.createElement("option");
    optionElement.value = option;
    optionElement.textContent = option;
    selectElement.appendChild(optionElement);
  });
}

// Populate state select input
const stateSelect = document.getElementById("stateSelect");
populateSelect(
  stateSelect,
  nigeriaStates.map((state) => state.name)
);

// Populate city select input based on selected state
const citySelect = document.getElementById("citySelect");
stateSelect.addEventListener("change", function () {
  const selectedState = stateSelect.value;
  const selectedStateData = nigeriaStates.find(
    (state) => state.name === selectedState
  );
  if (selectedStateData) {
    populateSelect(citySelect, selectedStateData.cities);
  } else {
    citySelect.innerHTML = '<option value="">Select City</option>';
  }
});

